package day08_ifStatements;

public class SwapTwoVariables_WithTemporaryVariable {
    public static void main(String[] args) {

        int a = 10 ;
        int b = 15;
        //------------------------------
        int c = b;
        b = a; //b=10
        a = c;// a=b = 15
        System.out.println("a = " + a);
        System.out.println("b = " + b);










    }

}

package day03_EscapeSequences;

 /*
 Escape sequences must be given with the double quote.
 \n: start a new line
 \t: tab (paragraph space)
\" : double quote
  */

public class EscapeSequences {
    public static void main(String[] args) {
        System.out.println(" java \npython \nc#");
        System.out.println("____________________");
        System.out.println(" Hello Cydeo \nhow are you all today \nit's nice to see you all! \nwhat class do we have next week? ");
        System.out.println("\tjava is cool programming language");
        System.out.println("_________________________________________");
        System.out.println("/\\"); // in order to print one backslash we need to give two backslash.
        System.out.println(" my favorite TV show is \"Game of throne\"");







    }









}
